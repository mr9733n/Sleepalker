from pptx import Presentation
from pptx.util import Inches, Pt

prs = Presentation()

def add_title_slide(title_text, subtitle_text):
    slide = prs.slides.add_slide(prs.slide_layouts[0])
    title = slide.shapes.title
    subtitle = slide.placeholders[1]
    title.text = title_text
    subtitle.text = subtitle_text

def add_title_body_slide(title_text, body_lines, add_image_placeholder=False):
    slide = prs.slides.add_slide(prs.slide_layouts[1])
    title = slide.shapes.title
    body = slide.shapes.placeholders[1]
    title.text = title_text
    body_tf = body.text_frame
    body_tf.clear()
    for i, line in enumerate(body_lines):
        if i == 0:
            p = body_tf.paragraphs[0]
            p.text = line
        else:
            p = body_tf.add_paragraph()
            p.text = line
        p.font.size = Pt(14)
    if add_image_placeholder:
        left = Inches(6.2)
        top = Inches(1)
        width = Inches(3)
        height = Inches(2.5)
        pic_box = slide.shapes.add_textbox(left, top, width, height)
        tf = pic_box.text_frame
        tf.text = "Image placeholder\n(–¥–æ–±–∞–≤–∏—Ç—å –∞—Ä—Ç/—Å–∫—Ä–∏–Ω—à–æ—Ç)"
        tf.paragraphs[0].font.size = Pt(12)

def add_two_column_slide(title_text, left_lines, right_lines):
    slide = prs.slides.add_slide(prs.slide_layouts[5])  # Title Only layout for custom boxes
    title = slide.shapes.title
    title.text = title_text
    left_box = slide.shapes.add_textbox(Inches(0.5), Inches(1.5), Inches(4.3), Inches(4.5))
    left_tf = left_box.text_frame
    left_tf.clear()
    for i, line in enumerate(left_lines):
        if i == 0:
            p = left_tf.paragraphs[0]
            p.text = line
        else:
            p = left_tf.add_paragraph()
            p.text = line
        p.font.size = Pt(13)
    right_box = slide.shapes.add_textbox(Inches(5.0), Inches(1.5), Inches(4.0), Inches(4.5))
    right_tf = right_box.text_frame
    right_tf.clear()
    for i, line in enumerate(right_lines):
        if i == 0:
            p = right_tf.paragraphs[0]
            p.text = line
        else:
            p = right_tf.add_paragraph()
            p.text = line
        p.font.size = Pt(13)

